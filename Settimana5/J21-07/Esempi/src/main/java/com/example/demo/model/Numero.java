package com.example.demo.model;

public class Numero
{
    private int num;

    // constructors
    public Numero() {
    }

    public Numero(int n)
    {
        num = n;
    }

    public void setNum(int newNum)
    {
        num = newNum;
    }

    public int getNum()
    {
        return num;
    }
}
